SELECT T.HOUR AS HOUR, COUNT(O.DATETIME) AS COUNT
FROM ANIMAL_OUTS O
RIGHT JOIN TEMP T ON HOUR(O.DATETIME) = T.HOUR 
GROUP BY T.HOUR
ORDER BY HOUR